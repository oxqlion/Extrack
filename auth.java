    public void SignIn(String uname, String pw) {
        try {
            if (checkUsername(uname)) {
                if (checkPassword(pw)) {
                    System.out.println("Logged In");
                    try {
                        Class.forName("com.mysql.cj.jdbc.Driver");
                        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/extrack", "root", "");
                        Statement stat = conn.createStatement();
                        ResultSet data = stat.executeQuery("SELECT * FROM users WHERE username = '" + uname + "';");
                        if (data.next()) {
                            int user_id = data.getInt("id");
                            one main = new one(user_id);
                            main.setVisible(true);
                            this.setVisible(false);
                        }

//            conn.close();
                    } catch (Exception e) {
                        System.out.print("Error : ");
                        e.printStackTrace();
                    }
                } else {
                    System.out.println("Wrong Password");
                }
            } else {
                System.out.println("Account Doesn't Exists");
            }
        } catch (Exception e) {
            System.out.print("Error : ");
            e.printStackTrace();
        }
    }

