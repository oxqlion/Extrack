    public void SignUp(String uname, String pw) {
        System.out.println("Hello World!");
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/extrack", "root", "");
            Statement stat = conn.createStatement();
//            int res = stat.executeUpdate("INSERT INTO users (`id`, `username`, `password`) VALUES ('', ?, ?);");

            String query = "INSERT INTO users (`id`, `username`, `password`) VALUES (NULL, ?, ?);";
            PreparedStatement statement = conn.prepareStatement(query);

            statement.setString(1, uname);
            statement.setString(2, pw);

            int rowsInserted = statement.executeUpdate();
            System.out.println(rowsInserted + " row(s) inserted.");

            ResultSet data = stat.executeQuery("SELECT * FROM users;");
            while (data.next()) {
                System.out.println(data.getString("username"));
            }
//            conn.close();
        } catch (Exception e) {
            System.out.print("Error : ");
            e.printStackTrace();
        }
    }

    public boolean checkUsername(String uname) {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/extrack", "root", "");
            Statement stat = conn.createStatement();
            ResultSet data = stat.executeQuery("SELECT username FROM users WHERE username = '" + uname + "';");
            while (data.next()) {
                System.out.println(data.getString("username"));
                if (data.getString("username").equals(uname)) {
                    return true;
                }
            }
//            conn.close();
        } catch (Exception e) {
            System.out.print("Error : ");
            e.printStackTrace();
        }

        return false;
    }

